<script setup>
import { logger } from '@/utils/logger'
import axios from "axios";
import { useToast } from 'primevue/usetoast'
import { onMounted, ref, watch } from 'vue'

const toast = useToast()
const questions = ref([])
const loading = ref(false)
const showForm = ref(false)
const editingQuestion = ref(null)

/* ================================================
   üî• axios ÂÆû‰æãÈÖçÁΩÆ
================================================ */
const api = axios.create({
  baseURL: "/api",
  timeout: 10000,
});

// ============ ËØ∑Ê±ÇÊã¶Êà™Âô®ÔºàÊ∑ªÂä† tokenÔºâ============
api.interceptors.request.use(
  (config) => {
    
    // üî• Ëá™Âä®Ê∑ªÂä† token Âà∞ËØ∑Ê±ÇÂ§¥
    const token = localStorage.getItem('token');
    if (token) {
      config.headers.Authorization = `Bearer ${token}`;
    }
    
    return config;
  },
  (error) => {
    logger.error('‚ùå Request Error:', error);
    return Promise.reject(error);
  }
);

// ============ ÂìçÂ∫îÊã¶Êà™Âô® ============
api.interceptors.response.use(
  (response) => {
    return response;
  },
  (error) => {
    logger.error('‚ùå API Error:', error.response?.data || error.message);
    
    // üî• Â§ÑÁêÜ 401 Êú™ÊéàÊùÉÈîôËØØ
    if (error.response?.status === 401) {
      localStorage.removeItem('token');
      localStorage.removeItem('playerId');
      localStorage.removeItem('playerName');
      window.location.href = '/login';
      return Promise.reject(error);
    }
    
    // üî• Ê£ÄÊü•ÊòØÂê¶ÈúÄË¶ÅÈùôÈªòÂ§ÑÁêÜÔºàÈÖçÁΩÆ‰∏≠ËÆæÁΩÆ‰∫Ü silentError: trueÔºâ
    const silentError = error.config?.silentError;
    
    // üî• ËøáÊª§‰∏çÈúÄË¶ÅÂÖ®Â±ÄÊèêÁ§∫ÁöÑÈîôËØØ
    const shouldShowToast = !silentError && !isIgnorableError(error);
    
    // Âè™ÊúâÈúÄË¶ÅÊèêÁ§∫ÁöÑÈîôËØØÊâçËß¶ÂèëÂÖ®Â±Ä‰∫ã‰ª∂
    if (shouldShowToast) {
      window.dispatchEvent(new CustomEvent('api-error', {
        detail: {
          message: error.response?.data?.message || error.message || 'ËØ∑Ê±ÇÂ§±Ë¥•',
          status: error.response?.status,
          url: error.config?.url
        }
      }));
    }
    
    return Promise.reject(error);
  }
);

// üî• Âà§Êñ≠ÊòØÂê¶ÊòØÂèØÂøΩÁï•ÁöÑÈîôËØØÔºà‰∏çÈúÄË¶ÅÂºπÁ™óÊèêÁ§∫Ôºâ
function isIgnorableError(error) {
  const status = error.response?.status;
  const message = error.response?.data?.message || '';
  const url = error.config?.url || '';
  
  // ÊàøÈó¥‰∏çÂ≠òÂú®Ôºà404Ôºâ- ÈùôÈªòÂ§ÑÁêÜ
  if (status === 404 && url.includes('/rooms/')) {
    return true;
  }
  
  // ÊàøÈó¥Â∑≤ÁªìÊùü/‰∏çÂ≠òÂú®Á≠â‰∏öÂä°ÈîôËØØ - ÈùôÈªòÂ§ÑÁêÜ
  if (message.includes('ÊàøÈó¥‰∏çÂ≠òÂú®') ||
      message.includes('ÊàøÈó¥Â∑≤ÁªìÊùü') ||
      message.includes('ÊàøÈó¥Â∑≤ËøáÊúü')) {
    return true;
  }
  
  // ÈáçÂ§çÊèê‰∫§Á≠âÊ≠£Â∏∏‰∏öÂä°ÈÄªËæë - ÈùôÈªòÂ§ÑÁêÜ
  if (message.includes('Â∑≤ÁªèÊèê‰∫§') ||
      message.includes('Â∑≤Êèê‰∫§')) {
    return true;
  }
  
  return false;
}

// Ë°®ÂçïÊï∞ÊçÆ
const form = ref({
  type: 'CHOICE',
  text: '',
  strategyId: '',
  minPlayers: 2,
  maxPlayers: 2,
  defaultCHOICE: '',
  
  // CHOICE ‰∏ìÁî®
  options: [
    { key: 'A', text: '' },
    { key: 'B', text: '' }
  ],
  
  // BID ‰∏ìÁî®
  min: 0,
  max: 100,
  step: 1,
  
  // Â∫èÂàóÈÖçÁΩÆÔºàÂ§çÈÄâÊ°ÜÊéßÂà∂Ôºâ
  isSequence: false,
  sequenceGroupId: '',
  sequenceOrder: 1,
  totalSequenceCount: 1,
  prerequisiteQuestionIds: '',
  
  // ÈáçÂ§çÈÖçÁΩÆÔºàÂ§çÈÄâÊ°ÜÊéßÂà∂Ôºâ
  isRepeatable: false,
  repeatTimes: 1,
  repeatInterval: null,
  repeatGroupId: ''
})

// ÁõëÂê¨Á±ªÂûãÂàáÊç¢ÔºåÊ∏ÖÁ©∫ÂØπÂ∫îÂ≠óÊÆµ
watch(() => form.value.type, (newType) => {
  if (newType === 'CHOICE') {
    form.value.min = null
    form.value.max = null
    form.value.step = null
  } else if (newType === 'BID') {
    form.value.options = []
  }
})

// ÁõëÂê¨Â∫èÂàóÂ§çÈÄâÊ°Ü
watch(() => form.value.isSequence, (isChecked) => {
  if (!isChecked) {
    form.value.sequenceGroupId = ''
    form.value.sequenceOrder = 1
    form.value.totalSequenceCount = 1
    form.value.prerequisiteQuestionIds = ''
  }
})

// ÁõëÂê¨ÈáçÂ§çÂ§çÈÄâÊ°Ü
watch(() => form.value.isRepeatable, (isChecked) => {
  if (!isChecked) {
    form.value.repeatTimes = 1
    form.value.repeatInterval = null
    form.value.repeatGroupId = ''
  }
})

// Âä†ËΩΩÈ¢òÁõÆÂàóË°®
const loadQuestions = async () => {
  loading.value = true
  try {
    const response = await api.get('/admin/questions')
    questions.value = response.data.content || response.data
    toast.add({
      severity: 'success',
      summary: 'Âä†ËΩΩÊàêÂäü',
      detail: `ÂÖ± ${questions.value.length} ÈÅìÈ¢òÁõÆ`,
      life: 2000
    })
  } catch (error) {
    logger.error('Âä†ËΩΩÂ§±Ë¥•', error)
    toast.add({
      severity: 'error',
      summary: 'Âä†ËΩΩÂ§±Ë¥•',
      detail: error.message,
      life: 3000
    })
  } finally {
    loading.value = false
  }
}

// ÊâìÂºÄÊñ∞Âª∫Ë°®Âçï
const openCreateForm = () => {
  editingQuestion.value = null
  form.value = {
    type: 'CHOICE',
    text: '',
    strategyId: '',
    minPlayers: 2,
    maxPlayers: 2,
    defaultCHOICE: '',
    options: [
      { key: 'A', text: '' },
      { key: 'B', text: '' }
    ],
    min: 0,
    max: 100,
    step: 1,
    isSequence: false,
    sequenceGroupId: '',
    sequenceOrder: 1,
    totalSequenceCount: 1,
    prerequisiteQuestionIds: '',
    isRepeatable: false,
    repeatTimes: 1,
    repeatInterval: null,
    repeatGroupId: ''
  }
  showForm.value = true
}

// ÊâìÂºÄÁºñËæëË°®Âçï
const openEditForm = (question) => {
  editingQuestion.value = question
  
  // üî• Ëß£Êûê optionsÔºàÊîØÊåÅÂ§öÁßçÊ†ºÂºèÔºâ
  let parsedOptions = []
  
  // ÊÉÖÂÜµ1ÔºöÂêéÁ´ØÁõ¥Êé•ËøîÂõû options Êï∞ÁªÑ
  if (Array.isArray(question.options) && question.options.length > 0) {
    parsedOptions = question.options
  }
  // ÊÉÖÂÜµ2ÔºöÂêéÁ´ØËøîÂõû optionsJson Â≠óÁ¨¶‰∏≤
  else if (question.optionsJson) {
    try {
      parsedOptions = JSON.parse(question.optionsJson)
    } catch (e) {
      logger.error('‚ùå Ëß£Êûê optionsJson Â§±Ë¥•:', e)
      parsedOptions = []
    }
  }
  // ÊÉÖÂÜµ3ÔºöÊ£ÄÊü•ÊòØÂê¶ÊúâÂ∫èÂàóÂåñÁöÑÂ≠óÁ¨¶‰∏≤Â≠óÊÆµ
  else if (typeof question.options === 'string') {
    try {
      parsedOptions = JSON.parse(question.options)
    } catch (e) {
      logger.error('‚ùå Ëß£ÊûêÂ≠óÁ¨¶‰∏≤ options Â§±Ë¥•:', e)
      parsedOptions = []
    }
  }
  
  // ÊÉÖÂÜµ4ÔºöCHOICE Á±ªÂûã‰ΩÜÊ≤°Êúâ optionsÔºåÁªô‰∏™ÈªòËÆ§ÂÄº
  if (parsedOptions.length === 0 && question.type === 'CHOICE') {
    parsedOptions = [{ key: 'A', text: '' }]
  }
  
  // üî• Â°´ÂÖÖË°®Âçï
  form.value = {
    // Âü∫Á°Ä‰ø°ÊÅØ
    type: question.type || 'CHOICE',
    text: question.text || '',
    strategyId: question.strategyId || '',
    minPlayers: question.minPlayers ?? 2,
    maxPlayers: question.maxPlayers ?? 2,
    defaultCHOICE: question.defaultCHOICE || '',
    
    // CHOICE ‰∏ìÁî®
    options: parsedOptions,
    
    // BID ‰∏ìÁî®
    min: question.min ?? 0,
    max: question.max ?? 100,
    step: question.step ?? 1,
    
    // Â∫èÂàóÈÖçÁΩÆ
    isSequence: !!(question.sequenceGroupId),
    sequenceGroupId: question.sequenceGroupId || '',
    sequenceOrder: question.sequenceOrder ?? 1,
    totalSequenceCount: question.totalSequenceCount ?? 1,
    prerequisiteQuestionIds: question.prerequisiteQuestionIds || '',
    
    // ÈáçÂ§çÈÖçÁΩÆ
    isRepeatable: !!question.isRepeatable,
    repeatTimes: question.repeatTimes ?? 1,
    repeatInterval: question.repeatInterval ?? null,
    repeatGroupId: question.repeatGroupId || ''
  }
  
  
  showForm.value = true
}

// Ê∑ªÂä†ÈÄâÈ°π
const addOption = () => {
  const nextKey = String.fromCharCode(65 + form.value.options.length)
  form.value.options.push({ key: nextKey, text: '' })
}

// Âà†Èô§ÈÄâÈ°π
const removeOption = (index) => {
  if (form.value.options.length > 1) {
    form.value.options.splice(index, 1)
  }
}

// Êèê‰∫§Ë°®Âçï
const submitForm = async () => {
  // È™åËØÅÂøÖÂ°´È°π
  if (!form.value.text || !form.value.strategyId) {
    toast.add({
      severity: 'warn',
      summary: 'ËØ∑Â°´ÂÜôÂøÖÂ°´È°π',
      detail: 'È¢òÁõÆÊñáÊú¨ÂíåÁ≠ñÁï•ID‰∏çËÉΩ‰∏∫Á©∫',
      life: 3000
    })
    return
  }
  
  // üî• È™åËØÅ CHOICE Á±ªÂûãÁöÑÈÄâÈ°π
  if (form.value.type === 'CHOICE') {
    if (!form.value.options || form.value.options.length === 0) {
      toast.add({
        severity: 'warn',
        summary: 'ËØ∑Ê∑ªÂä†ÈÄâÈ°π',
        detail: 'ÈÄâÊã©È¢òËá≥Â∞ëÈúÄË¶Å‰∏Ä‰∏™ÈÄâÈ°π',
        life: 3000
      })
      return
    }
    
    // üî• ËøáÊª§ÊéâÁ©∫ÈÄâÈ°π
    form.value.options = form.value.options.filter(opt => opt.key && opt.text)
    
    if (form.value.options.length === 0) {
      toast.add({
        severity: 'warn',
        summary: 'ÈÄâÈ°πÂÜÖÂÆπ‰∏çËÉΩ‰∏∫Á©∫',
        detail: 'ËØ∑Â°´ÂÜôÈÄâÈ°πÁöÑ key Âíå text',
        life: 3000
      })
      return
    }
  }
  
  // üî• È™åËØÅ BID Á±ªÂûãÁöÑËåÉÂõ¥
  if (form.value.type === 'BID') {
    if (form.value.min == null || form.value.max == null) {
      toast.add({
        severity: 'warn',
        summary: 'ËØ∑ËÆæÁΩÆËåÉÂõ¥',
        detail: 'Á´û‰ª∑È¢òÈúÄË¶ÅËÆæÁΩÆÊúÄÂ∞èÂÄºÂíåÊúÄÂ§ßÂÄº',
        life: 3000
      })
      return
    }
  }
  
  // ÊûÑÂª∫Êèê‰∫§Êï∞ÊçÆ
  const payload = {
    type: form.value.type,
    text: form.value.text,
    strategyId: form.value.strategyId,
    minPlayers: form.value.minPlayers,
    maxPlayers: form.value.maxPlayers,
    defaultCHOICE: form.value.defaultCHOICE,
    
    // CHOICE ‰∏ìÁî®
    options: form.value.type === 'CHOICE' && form.value.options.length > 0 
      ? form.value.options 
      : null,
    
    // BID ‰∏ìÁî®
    min: form.value.type === 'BID' ? form.value.min : null,
    max: form.value.type === 'BID' ? form.value.max : null,
    step: form.value.type === 'BID' ? form.value.step : null,
    
    // Â∫èÂàóÈÖçÁΩÆ
    sequenceGroupId: form.value.isSequence ? form.value.sequenceGroupId : null,
    sequenceOrder: form.value.isSequence ? form.value.sequenceOrder : null,
    totalSequenceCount: form.value.isSequence ? form.value.totalSequenceCount : null,
    prerequisiteQuestionIds: form.value.isSequence ? form.value.prerequisiteQuestionIds : null,
    
    // ÈáçÂ§çÈÖçÁΩÆ
    isRepeatable: form.value.isRepeatable,
    repeatTimes: form.value.isRepeatable ? form.value.repeatTimes : null,
    repeatInterval: form.value.isRepeatable ? form.value.repeatInterval : null,
    repeatGroupId: form.value.isRepeatable ? form.value.repeatGroupId : null
  }

  loading.value = true
  try {
    if (editingQuestion.value) {
      await api.put(`/admin/questions/${editingQuestion.value.id}`, payload)
    } else {
      await api.post('/admin/questions', payload)
    }
    
    toast.add({
      severity: 'success',
      summary: editingQuestion.value ? 'Êõ¥Êñ∞ÊàêÂäü' : 'ÂàõÂª∫ÊàêÂäü',
      life: 2000
    })
    
    showForm.value = false
    loadQuestions()
  } catch (error) {
    logger.error('Êìç‰ΩúÂ§±Ë¥•', error)
    toast.add({
      severity: 'error',
      summary: 'Êìç‰ΩúÂ§±Ë¥•',
      detail: error.response?.data?.message || error.message,
      life: 3000
    })
  } finally {
    loading.value = false
  }
}

// Âà†Èô§È¢òÁõÆ
const deleteQuestion = async (id) => {
  if (!confirm('Á°ÆÂÆöÂà†Èô§ËøôÈÅìÈ¢òÁõÆÂêóÔºü')) return
  
